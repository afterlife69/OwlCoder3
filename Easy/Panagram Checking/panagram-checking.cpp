class Solution
{
  public:
    //Function to check if a string is Pangram or not.
    bool checkPangram (string s) {
        int cur = 0;
        for(auto it : s){
            if(it >= 65 && it <= 90)cur |= (1 << it-'A');
            if(it >= 97 && it <= 122)cur |= (1 << it-'a');
        }
        return cur == (1 << 26) -1 ;
    }

};

//{ Driver Code Starts.

// Driver Program to test above functions
int main()
{
    int t;
    cin>>t;
    cin.ignore(INT_MAX, '\n');
    while(t--){
        string s;
        getline(cin, s);
        Solution obj;
        if (obj.checkPangram(s) == true)
            cout<<1<<endl;
        else
            cout<<0<<endl;
    }
    return(0);
}

